# Operator Perbandingan : ==, !==, >, <, >=, <=, 

## 1 Operator Sama dengan contoh Simbolnya:  ==
samaDengan = 10 == 10
print('1. Hasil:', samaDengan)

## 2 Operator Perbandingan Tidak Sama dengan
tidaSamaDengan = 10 != 10
print('2. Hasil:', tidaSamaDengan)

## 3 Operator Perbandingan Lebih dari
lebihDari = 10 > 10
print('3. Hasil:', lebihDari)

## 4 Operator Perbandingan Kurang dari
kurangDari = 10 < 5
print('4. Hasil:', kurangDari)

# 5 Operator Perbandingan Lebih dari sama dengan
lebihDariSamaDengan = 10 >= 10
print("5. Hasil:", lebihDariSamaDengan)

# 6 Operator Perbandingan Kurang dari sama dengan
kurangDariSamaDengan = 10 <= 8
print('6. Hasil:',  kurangDariSamaDengan)